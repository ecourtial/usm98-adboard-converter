package com.ecourtial.usm98addboards.tools;

public class BmpPixel {

    private final int r;
    private final int g;
    private final int b;

    public BmpPixel(int r, int g, int b) {
        this.r = r;
        this.g = g;
        this.b = b;
    }

    public int getR() {
        return r;
    }

    public int getG() {
        return g;
    }

    public int getB() {
        return b;
    }

}
